package com.installedapps.com.installedapps;

public class PermissionModel {
    private String name;
    private boolean status;

    public PermissionModel(String name, boolean status) {
        this.name = name;
        this.status = status;
    }

    public String getName() {
        return name;
    }


    public boolean getPackageName() { return status; }
}
